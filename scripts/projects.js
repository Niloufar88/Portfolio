const projectNamesContainer = document.querySelector(".project-names");
const aboutTitle = document.getElementById("about-title");
const projectDuration = document.getElementById("project-duration");
const aboutText = document.getElementById("about-text");
const organiseTitle = document.getElementById("organise-title");
const organiseText = document.getElementById("organise-text");
const learnTitle = document.getElementById("learn-title");
const learnText = document.getElementById("learn-text");
const techTitle = document.getElementById("tech-title");
const techImagesContainer = document.querySelector(".tech-icon");
const techImages = document.querySelectorAll(".tech-icon img");
const projectPoster = document.querySelector("#project-photo img");
const liveTestBtn = document.getElementById("liveTestBtn");
const gitHubBtn = document.getElementById("gitHubBtn");

window.currentActiveProjectId = "project1"; // Default active project

projectNamesContainer.addEventListener("click", (event) => {
  const clickedTab = event.target.closest(".project-name-tab");
  if (!clickedTab) return;

  removeActiveClass();
  clickedTab.classList.add("active");

  const projectId = clickedTab.getAttribute("data-id");
  window.currentActiveProjectId = projectId; // Update the current active project ID
  window.updateProjectDisplay(projectId);
});

window.updateProjectDisplay = function (projectId) {
  const project = projectsData.find((project) => project.id === projectId);
  if (!project) return;

  const language = localStorage.getItem("prefferdLanguage") || "de";
  if (project) {
    renderProjectData(language, project);
    renderProjectImages(project);
    updateProjectLinks(project);
  }
};

function removeActiveClass() {
  const projectTabs = document.querySelectorAll(".project-name-tab");

  projectTabs.forEach((tab) => {
    tab.classList.remove("active");
  });
}

function renderProjectData(language, project) {
  aboutTitle.textContent = project[language].aboutTitle;
  projectDuration.textContent = project[language].durationText;
  aboutText.textContent = project[language].aboutText;
  organiseTitle.textContent = project[language].organiseTitle;
  organiseText.textContent = project[language].organiseText;
  learnTitle.textContent = project[language].learnTitle;
  learnText.textContent = project[language].learnText;
  techTitle.textContent = project[language].techTitle;
}

function renderProjectImages(project) {
  projectPoster.src = project.projectImage;
  techImagesContainer.innerHTML = ""; // Clear previous images
  project.techImages.forEach((image) => {
    const techImg = document.createElement("img");
    techImg.src = image;
    techImagesContainer.appendChild(techImg);
  });
}

function updateProjectLinks(project) {
  liveTestBtn.onclick = () => navigateToServer(project.projectLink);
  gitHubBtn.onclick = () => navigateToGithub(project.gitHubLink);
}

document.addEventListener("DOMContentLoaded", () => {
  window.updateProjectDisplay(window.currentActiveProjectId);
});
